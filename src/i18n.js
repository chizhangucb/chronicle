const DICTS = { zh, ja };

export function lang() {
  return localStorage.getItem('chronicle-lang') || 'en';
}

export function t(s) {
  const d = DICTS[lang()];
  return d ? (d[s] ?? s) : s;
}

export function setLang(next) {
  if (next === lang()) return;
  localStorage.setItem('chronicle-lang', next);
  location.reload();
}
